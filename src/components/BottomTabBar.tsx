const COLOR_ACTIVE = '#FF5C8D';
const COLOR_INACTIVE = '#9E9E9E';

export default function BottomTabBar() {
  const pathname = usePathname();

  // Hide tab bar on certain pages
  const hiddenPaths = ['/login', '/signup', '/auth', '/seller', '/shop/', '/explore/category/'];
  const shouldHide = hiddenPaths.some((path) => pathname.startsWith(path));
  if (shouldHide) return null;

  return (
    <nav
      id="bottom-nav-bar"
      className="sticky bottom-0 z-50 safe-area-bottom"
      style={{
        background: '#FFFFFF',
        borderTop: '1px solid #F5F5F5',
      }}
    >
      <div style={{
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center',
        padding: '8px',
      }}>
        {tabs.map((tab) => {
          const isActive =
            tab.href === '/'
              ? pathname === '/'
              : pathname.startsWith(tab.href);
          const color = isActive ? COLOR_ACTIVE : COLOR_INACTIVE;

          return (
            <Link
              key={tab.key}
              href={tab.href}
              style={{
                display: 'flex',
                flex: 1,
                flexDirection: 'column',
                alignItems: 'center',
                gap: '0px',
                textDecoration: 'none',
                color,
                transition: 'color 0.15s ease',
              }}
            >
              <div style={{
                display: 'flex',
                alignItems: 'center',
                justifyContent: 'center',
                padding: '4px',
              }}>
                <tab.Icon color={color} />
              </div>
              <span style={{
                fontFamily: 'Pretendard, -apple-system, sans-serif',
                fontSize: '11px',
                fontWeight: 500,
                lineHeight: '125%',
              }}>
                {tab.label}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
